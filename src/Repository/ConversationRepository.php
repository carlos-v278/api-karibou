class ConversationRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Conversation::class);
    }

    public function findByParticipants(array $participants): array
    {
        $qb = $this->createQueryBuilder('c');

        $qb->select('c')
            ->leftJoin('c.participants', 'p')
            ->groupBy('c.id')
            ->having($qb->expr()->eq($qb->expr()->count('p'), ':participantCount'))
            ->setParameter('participantCount', count($participants));

        foreach ($participants as $key => $participant) {
            $parameterName = 'participant'.$key;
            $qb->andWhere(":$parameterName MEMBER OF c.participants")
                ->setParameter($parameterName, $participant);
        }

        return $qb->getQuery()->getResult();
    }

